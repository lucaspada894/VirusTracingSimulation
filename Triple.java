public class Triple {

	protected int ci;
	protected int cj;
	protected int timestamp;
	
	public Triple(int ci, int cj, int timestamp) {
		this.ci = ci;
		this.cj = cj;
		this.timestamp = timestamp;
	}
	
	
	public int getCi() {
		return this.ci;
	}
	
	public int getCj() {
		return this.cj;
	}
	
	public int getTimestamp() {
		return this.timestamp;
	}
	
	
	

 
}

